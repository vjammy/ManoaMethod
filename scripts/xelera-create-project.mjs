#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
const name = (process.argv.find((a) => a.startsWith('--name=')) || '--name=New Product').split('=')[1];
const out = path.resolve((process.argv.find((a) => a.startsWith('--out=')) || `--out=${name.toLowerCase().replace(/[^a-z0-9]+/g, '-')}`).split('=')[1]);
fs.mkdirSync(out, { recursive: true });
fs.writeFileSync(path.join(out, 'README.md'), `# ${name}\n\nGenerated by Xelera Method CLI.\n`);
console.log(`Created ${out}`);
